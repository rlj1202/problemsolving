#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(false); cin.tie(nullptr); cout.tie(nullptr);

    int T;
    cin >> T;

    while (T--) {
        int N;
        cin >> N;

        for (int n = 0; n < N; n++) {
            int l, r, c;
            cin >> l >> r >> c;
        }

        cout << '\n';
    }

    return 0;
}
